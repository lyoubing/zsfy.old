        {
            int inputInt = (int)inputNumber;

            if (Math.Abs(inputInt) > 0)
            {
                if (inputInt.ToString().Length > validPos - dotPos)
                {
                    return false;
                }
            }

            string[] temp = inputNumber.ToString().Split('.');

            if (temp.Length <= 1) 
            {
                return true;
            }

            return temp[1].Length <= dotPos;
        }
